--이미 테이블이 존재할 경우 제거 
--테이블이 없으면 오류 발생 (상관은 없음) 
DROP TABLE TB_REVIEW CASCADE CONSTRAINTS;
DROP TABLE TB_ORDER_ITEM CASCADE CONSTRAINTS;
DROP TABLE TB_ORDER CASCADE CONSTRAINTS;
DROP TABLE TB_PRODUCT CASCADE CONSTRAINTS;
DROP TABLE TB_CUSTOMER CASCADE CONSTRAINTS;  
DROP TABLE TB_FEEDBACK CASCADE CONSTRAINTS; 
DROP TABLE TB_MENU CASCADE CONSTRAINTS;  
DROP TABLE TB_BRANCH_SALES CASCADE CONSTRAINTS;

-- 1. 고객 테이블
CREATE TABLE TB_CUSTOMER (
    CUSTOMER_ID   NUMBER PRIMARY KEY,
    NAME          VARCHAR2(50),
    EMAIL         VARCHAR2(100),
    CREATED_AT    DATE DEFAULT SYSDATE,
    STATUS        VARCHAR2(10) CHECK (STATUS IN ('ACTIVE', 'INACTIVE'))
);

-- 2. 상품 테이블
CREATE TABLE TB_PRODUCT (
    PRODUCT_ID    NUMBER PRIMARY KEY,
    NAME          VARCHAR2(100),
    CATEGORY      VARCHAR2(50),
    PRICE         NUMBER(10, 2),
    STOCK         NUMBER
);

-- 3. 주문 테이블
CREATE TABLE TB_ORDER (
    ORDER_ID      NUMBER PRIMARY KEY,
    CUSTOMER_ID   NUMBER ,
    ORDER_DATE    DATE DEFAULT SYSDATE,
    TOTAL_AMOUNT  NUMBER(10, 2),
    STATUS        VARCHAR2(20)
);

-- 4. 주문 상세 테이블
CREATE TABLE TB_ORDER_ITEM (
    ORDER_ITEM_ID  NUMBER PRIMARY KEY,
    ORDER_ID       NUMBER ,
    PRODUCT_ID     NUMBER ,
    QUANTITY       NUMBER ,
    PRICE          NUMBER(10, 2)
);

-- 5. 리뷰 테이블
CREATE TABLE TB_REVIEW (
    REVIEW_ID     NUMBER PRIMARY KEY,
    PRODUCT_ID    NUMBER ,
    CUSTOMER_ID   NUMBER ,
    RATING        NUMBER(1),
    REVIEW_DATE   DATE DEFAULT SYSDATE,
    USER_COMMENT  VARCHAR2(4000)
);

--6. 고객 피드백 테이블 
 CREATE TABLE TB_FEEDBACK (
    FEEDBACK_ID   NUMBER PRIMARY KEY,
    CUSTOMER_ID   NUMBER,
    SUBJECT       VARCHAR2(200),
    MESSAGE       VARCHAR2(4000),
    CREATED_AT    DATE DEFAULT SYSDATE,
    RESPONSE      VARCHAR2(4000)
);

--7. 메뉴 테이블
CREATE TABLE TB_MENU (
    MENU_ID        VARCHAR2(10) PRIMARY KEY,
    PARENT_MENU_ID VARCHAR2(10),
    MENU_NAME      VARCHAR2(100)
);

--8. 매출 테이블 
CREATE TABLE TB_BRANCH_SALES (
    BRANCH_CODE  CHAR(3),
    BRANCH_NAME  VARCHAR2(100),
    SALES_YM     CHAR(6),
    SALES_AMT    NUMBER
);


